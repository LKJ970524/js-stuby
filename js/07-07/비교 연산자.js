// 비교 연산자(Comparison)

const a = 1
const b = 3

// 동등하다 (형 변환!)
console.log(a == b); // false

// 부등하다 (형 변환!)
console.log(a != b); // true
// 상위 두개는 사용을 하지 않는 편이 좋다


// 일치
console.log(a === b); // false

// 불일치
console.log(a !== b); // true

// 크다
console.log(a > b); // false

// 크거나 같다
console.log(a >= b); // false

// 작다
console.log(a < b); // true

// 작거나 같다
console.log(a <= b); // true